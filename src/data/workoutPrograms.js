// Utility to migrate these programs to localStorage for testing
export function migrateWorkoutProgramsToLocalStorage() {
  const programs = workoutPrograms.map(wp => ({
    id: wp.id,
    title: wp.days[0]?.title || wp.id, // Use first day title as a placeholder for title
    category: wp.category,
    warmupMovements: wp.days[0]?.exercises || [],
    workoutMovements: wp.days[1]?.exercises || [],
    stretchingMovements: wp.days[2]?.exercises || [],
    challengeMovements: wp.days[3]?.exercises || [],
    author: 'system',
    dateCreated: new Date().toISOString(),
  }));
  localStorage.setItem('workouts', JSON.stringify(programs));
} 